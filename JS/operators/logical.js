// Javascript Logical Operator :-
/*
Logical operators perform logical operations and return a boolean value, either true or false.

const x = 5, y = 3;
(x < 6) && (y < 5); // true

( Here, && is the logical operator AND. Since both x < 6 and y < 5 are true, the result is true. )
( Logical operators perform logical operations: AND, OR and NOT. )
( Logical operators are used in decision making and loops. )
*/

/*
Operator	       Description	                                                   Example

&&	    Logical AND: true if both the operands/boolean values are true,         true && false; // false
        else evaluates to false	                                                (10==20 && 20==33) = false

||	    Logical OR: true if either of the operands/boolean values is true.      true || false; // true
        evaluates to false if both are false                                    (10==20 || 20==33) = false

!	    Logical NOT: true if the operand is false and vice-versa.	            !true; // false
                                                                                (!(10==20) = true
*/