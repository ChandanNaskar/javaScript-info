// Others Operators :-

/*
Operator	Description	                                     Example

,	        evaluates multiple operands and
            returns the value of the last operand.	          let a = (1, 3 , 4); // 4

?:	        returns value based on the condition
            It is like if-else.               	              (5 > 3) ? 'success' : 'error'; // "success"

delete	    deletes an object's property, or
            an element of an array	                          delete x

typeof	    returns a string indicating the data type	      typeof 3; // "number"

void	    discards the expression's return value	          void(x)

in	        returns true if the specified property
            is in the object	                              prop in object

instanceof	returns true if the specified object is
            of of the specified object type	                  object instanceof object_type
*/