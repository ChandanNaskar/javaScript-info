// JavaScript Bitwise Operators :-

/*
Bitwise operators treat its operands as a set of 32-bit binary digits (zeros and ones) and perform actions.
However, the result is shown as a decimal value.
*/

/*
Operators	Name	                            Example

&	       Bitwise AND	                       x & y (10==20 & 20==33) = false

|	       Bitwise OR	                       x | y (10==20 | 20==33) = false

^	       Bitwise XOR	                       x ^ y (10==20 ^ 20==33) = false

~	       Bitwise NOT	                       ~x (~10) = -10

<<	       Left shift	                       x << y (10<<2) = 40

>>	       Sign-propagating right shift	       x >> y (10>>2) = 2

>>>	       Zero-fill right shift	           x >>> y 	(10>>>2) = 2
*/