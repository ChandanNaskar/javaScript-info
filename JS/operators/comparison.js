// JavaScript Comparison / Relation Operators :-

/*
Comparison operators compare two values and give back a boolean value: either true or false.
Comparison operators are used in decision making and loops.

const a = 3, b = 2;
console.log(a > b); // true
( the comparison operator > is used to compare whether a is greater than b. )
*/

/*
Operator	        Description	                                                    Example

    ==	    Equal to: true if the operands are equal	                         5==5; //true

    !=	    Not equal to: true if the operands are not equal	                 5!=5; //false

    ===	    Strict equal to: true if the operands are equal and of
            the same type	                                                     5==='5'; //false

    !==	    Strict not equal to: true if the operands are equal but of
            different type or not equal at all	                                 5!=='5'; //true

    >	    Greater than: true if the left operand is greater than
            the right operand	                                                 3>2; //true

    >=	    Greater than or equal to: true if the left operand is
            greater than or equal to the right operand	                         3>=3; //true

    <	    Less than: true if the left operand is less than
            the right operand	                                                 3<2; //false

    <=	    Less than or equal to: true if the left operand is less than
            or equal to the right operand	                                     2<=2; //true
*/

// Comparison operators in JavaScript :-
/*
// equal operator
console.log(2 == 2); // true
console.log(2 == '2'); // true

// not equal operator
console.log(3 != 2); // true
console.log('hello' != 'Hello'); // true

// strict equal operator
console.log(2 === 2); // true
console.log(2 === '2'); // false

// strict not equal operator
console.log(2 !== '2'); // true
console.log(2 !== 2); // false
*/